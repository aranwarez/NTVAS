package com.model;

public class AccCenter {
	private int SN;
	private String ACC_CEN_CODE;
	private String DESCRIPTION;
	private String REGION_CODE;
	private String ERP_ACC_CEN_CD;
	private String USER;
	public int getSN() {
		return SN;
	}
	public void setSN(int sN) {
		SN = sN;
	}
	public String getACC_CEN_CODE() {
		return ACC_CEN_CODE;
	}
	public void setACC_CEN_CODE(String aCC_CEN_CODE) {
		ACC_CEN_CODE = aCC_CEN_CODE;
	}
	public String getDESCRIPTION() {
		return DESCRIPTION;
	}
	public void setDESCRIPTION(String dESCRIPTION) {
		DESCRIPTION = dESCRIPTION;
	}
	public String getREGION_CODE() {
		return REGION_CODE;
	}
	public void setREGION_CODE(String rEGION_CODE) {
		REGION_CODE = rEGION_CODE;
	}
	public String getERP_ACC_CEN_CD() {
		return ERP_ACC_CEN_CD;
	}
	public void setERP_ACC_CEN_CD(String eRP_ACC_CEN_CD) {
		ERP_ACC_CEN_CD = eRP_ACC_CEN_CD;
	}
	public String getUSER() {
		return USER;
	}
	public void setUSER(String uSER) {
		USER = uSER;
	}
}

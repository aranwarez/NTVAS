package com.model;

public class CollCenter {
	private int SN;
	private String CC_CODE;
	private String DESCRIPTION;
	private String ERP_CC_CD;
	private String ACC_CEN_CODE;
	private String CC_TYPE;
	private String USER;
	private String OFFICE_CODE;
	public int getSN() {
		return SN;
	}
	public void setSN(int sN) {
		SN = sN;
	}
	public String getCC_CODE() {
		return CC_CODE;
	}
	public void setCC_CODE(String cC_CODE) {
		CC_CODE = cC_CODE;
	}
	public String getDESCRIPTION() {
		return DESCRIPTION;
	}
	public void setDESCRIPTION(String dESCRIPTION) {
		DESCRIPTION = dESCRIPTION;
	}
	public String getERP_CC_CD() {
		return ERP_CC_CD;
	}
	public void setERP_CC_CD(String eRP_CC_CD) {
		ERP_CC_CD = eRP_CC_CD;
	}
	public String getACC_CEN_CODE() {
		return ACC_CEN_CODE;
	}
	public void setACC_CEN_CODE(String aCC_CEN_CODE) {
		ACC_CEN_CODE = aCC_CEN_CODE;
	}
	public String getCC_TYPE() {
		return CC_TYPE;
	}
	public void setCC_TYPE(String cC_TYPE) {
		CC_TYPE = cC_TYPE;
	}
	public String getUSER() {
		return USER;
	}
	public void setUSER(String uSER) {
		USER = uSER;
	}
	public String getOFFICE_CODE() {
		return OFFICE_CODE;
	}
	public void setOFFICE_CODE(String oFFICE_CODE) {
		OFFICE_CODE = oFFICE_CODE;
	}
}
